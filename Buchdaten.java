public class Buchdaten {
	private String Name;
	private double Preis;
	private double Bar;
	private double Zurück;
	private int Barcode;
	private String Genre;

	
	public Buchdaten(String name, double preis, double bar, double zurück, int barcode , String genre) {
		super();
		Name = name;
		Preis = preis;
		Bar = bar;
		Zurück = zurück;
		Barcode = barcode;
		Genre = genre;
		
	}
}